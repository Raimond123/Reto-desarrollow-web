    REG_AGUA_TEXTURA VARCHAR(100),
    REG_AGUA_PESO_NETO DECIMAL(10,2),
    REG_AGUA_FECHA_VENC DATE,
    REG_AGUA_ACIDEZ DECIMAL(5,2),
    REG_AGUA_CLORO_RESIDUAL DECIMAL(5,2),         
    REG_AGUA_CENIZAS DECIMAL(5,2),
